package loopStudy;

public class WhileLoop {

	public static void main(String[] args) {
		int b=4; //initialization of variable
		
		while (b<=40) //condition 
		{
			System.out.println(b);//<---printing statement
			b=b+4;//updation
		}
		
		int a=60;
		
		while (a>=6) 
		{
			System.out.println(a);
			a=a-6;
		}

	}

}
