package controlStatements;

public class IfElseStudy {

	public static void main(String[] args) {
		// If my marks are greater than or equal to 40 then I am pass else I am fail
		int marks=40;
		if (marks>=40) {
			System.out.println("I am pass");
		}
		else 
		{
			System.out.println("I am fail");
		}
		
		// If my grade is A i got above 90% marks else i got below 90%
		char grade='a';
		if (grade<=90) 
		{
			System.out.println("I got above 90%");
		}
		else 
		{
			System.out.println("I got below 90%");
		}
	

	}

}
